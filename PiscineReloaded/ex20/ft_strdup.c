#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	int		size;
	char	*tab;

	size = ft_strlen(src);
	tab = malloc(sizeof(char) * size + 1);
	if (tab == NULL)
		return (NULL);
	ft_strcpy(tab, src);
	return (tab);
}
